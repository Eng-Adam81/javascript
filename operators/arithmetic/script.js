var a = 10;
var b = 5;

// Addition (+)
var sum = a + b;
console.log("Sum:", sum); // 15

// Subtraction (-)
var difference = a - b;
console.log("Difference:", difference); // 5

// Multiplication (*)
var product = a * b;
console.log("Product:", product); // 50

// Division (/)
var quotient = a / b;
console.log("Quotient:", quotient); // 2

// Modulus (Remainder) (%)
var remainder = a % b;
console.log("Remainder:", remainder); // 0

// Exponentiation (**)
var power = a ** b;
console.log("Power:", power); // 100000

// Increment (++)
a++;
console.log("Incremented a:", a); // 11

// Decrement (--)
b--;
console.log("Decremented b:", b); // 4
